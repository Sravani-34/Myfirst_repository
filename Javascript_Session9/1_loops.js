/*JavaScript Controlling(Looping) Statements
--------------------------------------------
Loops in JavaScript are used to execute the same block of code a 
    *specified number of times*
     or 
    *while a specified condition is true*.


<<JavaScript Loops>>

when you write code, you want the same block of code to run over and over again in a row.
Instead of adding several almost equal lines in a script we can use loops to perform a task like this.

In JavaScript there are two different kind of loops:

    
    # while - loop
    through a block of code while a specified condition is true

    # do...while Loop
    This loop will always execute a block of code ONCE, and then it will repeat the loop as long as the specified condition is true.

    # for - loop
    through a block of code a specified number of times

            #for loop with in operator
            #for loop with of operator*/