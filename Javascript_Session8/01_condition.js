/*Conditional Statements
------------------------------

you want to perform different actions for different decisions

In JavaScript we have the following conditional statements:

> if statement - 
    if you want to execute some code only if a *specified condition* is true

> if...else statement - 
    if you want to execute some code if the condition is true and *another* code if the condition is false

> if...else if....else statement - 
    if you want to select one of many blocks of code to be executed

> switch statement - 
    if you want to select one of many blocks of code to be executed*/