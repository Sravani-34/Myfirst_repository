//Manipulate Arrays with .push() 
   

/*.push() method in JavaScript that adds one or more elements 
to the "end" of an array and returns the new length of the array. */


var ourArray = ["Sravani", "Sambha", "Laasya"];

ourArray.push(["We are family"]);

console.log(ourArray);
////Manipulate Arrays with pop()  

/*The pop() method of Array instances removes the "last" element from an array and returns that element. */


